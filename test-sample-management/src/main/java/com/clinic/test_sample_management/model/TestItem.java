package com.clinic.test_sample_management.model;

public class TestItem {
    private String name;
    private int price;

    public TestItem() {}

    public TestItem(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
